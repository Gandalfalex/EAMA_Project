package com.example.meetforsport.ui.EventCreator.DataHolder;

public class DataHolder {
    private int id;
    private String name;
    public DataHolder(int id, String name){
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
